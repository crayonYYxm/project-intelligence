def bug_design(bug_id: str, name: str) -> str:
    return f"""# {bug_id} {name}

## 1 BUG 现象

调用服务时返回旧值，期望返回新值。

## 2 原因分析

`src/service.py` 中的 `answer` 仍返回旧常量。

## 3 修复方案

### 3.1 改造思路

只调整 `answer` 的返回值，保持函数签名不变。

### 3.2 新旧代码对照

旧逻辑返回 41，新逻辑返回 42。

### 3.3 逻辑变更说明

所有调用方继续使用原函数并获得新值。

## 4 影响范围

仅影响 `src/service.py` 的 `answer`。

## 5 风险评估

**风险等级**：低

**极端预测**：旧调用方可能依赖错误常量。

**紧急举措**：回退本次常量修改。
"""
